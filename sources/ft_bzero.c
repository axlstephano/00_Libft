#include "../includes/libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	while (n--)
		*ptr++ = '\0';
}
//Para recorrer bloques de memoria sin conocer su tipo exacto, 
//size_t se usa como un tipo seguro 
//para representar desplazamientos dentro de la memoria.
